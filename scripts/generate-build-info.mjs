import { execSync } from 'node:child_process';
import { readFileSync, writeFileSync } from 'node:fs';
import { resolve } from 'node:path';

function getGitSha() {
  try {
    return execSync('git rev-parse --short HEAD', {
      cwd: process.cwd(),
      encoding: 'utf8',
      stdio: ['ignore', 'pipe', 'ignore'],
    }).trim();
  } catch {
    return 'nogit';
  }
}

function getUtcTimestamp() {
  const now = new Date();
  const year = String(now.getUTCFullYear());
  const month = String(now.getUTCMonth() + 1).padStart(2, '0');
  const day = String(now.getUTCDate()).padStart(2, '0');
  const hours = String(now.getUTCHours()).padStart(2, '0');
  const minutes = String(now.getUTCMinutes()).padStart(2, '0');
  const seconds = String(now.getUTCSeconds()).padStart(2, '0');

  return `${year}-${month}-${day}T${hours}:${minutes}:${seconds}Z`;
}

function getVersion() {
  try {
    const packageJsonPath = resolve(process.cwd(), 'package.json');
    const packageJson = JSON.parse(readFileSync(packageJsonPath, 'utf8'));
    return packageJson.version ?? '0.0.0';
  } catch {
    return '0.0.0';
  }
}

function main() {
  const gitSha = getGitSha();
  const buildTime = getUtcTimestamp();
  const version = getVersion();

  const buildInfoContent = `// Auto-generated by scripts/generate-build-info.mjs
// Do not edit manually - this file is regenerated on each build
// Generated: ${buildTime}

export const buildInfo = {
  version: '${version}',
  gitSha: '${gitSha}',
  buildTime: '${buildTime}',
} as const;
`;

  const buildInfoPath = resolve(
    process.cwd(),
    'src',
    'config',
    'build-info.ts'
  );
  writeFileSync(buildInfoPath, buildInfoContent, 'utf8');
  console.log(`Generated build info: ${version} (${gitSha}) at ${buildTime}`);
}

main();
